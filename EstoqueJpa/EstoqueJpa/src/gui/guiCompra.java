    private void formWindowActivated(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowActivated
        // TODO add your handling code here:
        List<Fornecedor> l = daoFornecedor.findFornecedorEntities();

        cboFornecedor.removeAllItems();

        for (Fornecedor f : l) {
            cboFornecedor.addItem(f);
        }

        List<Produto> l2 = daoProduto.findProdutoEntities();
        cboProduto.removeAllItems();

        for (Produto p : l2) {
            cboProduto.addItem(p);
        }


    }//GEN-LAST:event_formWindowActivated

    private void btnCriarNfActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCriarNfActionPerformed
        // TODO add your handling code here:
        compra = new Compra();
        compra.setFornecedor((Fornecedor) cboFornecedor.getSelectedItem());
        compra.setNumeroNotaFiscal(Integer.parseInt(txtNf.getText()));
        compra.setValorTotal(0.0);

        Date d = (Date) TxtData.getValue();
        Calendar c = Calendar.getInstance();
        c.setTime(d);

        compra.setDataCompra(c);

        daoCompra.create(compra);

    }//GEN-LAST:event_btnCriarNfActionPerformed

    private void BtnAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnAdicionarActionPerformed
        // TODO add your handling code here:

        Produto p = (Produto) cboProduto.getSelectedItem();
        Integer q = new Integer(TxtQuantidade.getText());

        daoCompra.adicionarProduto(compra, p, q);

        TxtTotal.setText(compra.getValorTotal().toString());

        List<DetalheCompra> ldc = daoCompra.getDetalhesDeUmaCompra(compra);
        DefaultTableModel dm = (DefaultTableModel) tabela.getModel();
        while (dm.getRowCount() > 0) {
            dm.removeRow(0);
        }
        for (DetalheCompra dc : ldc) {
            Object linha[] = {dc.getProduto().getDescricao(), dc.getProduto().getPrecoVenda(), dc.getQuantidade(), dc.getQuantidade() * dc.getValorTotal()};
            DefaultTableModel modelo = (DefaultTableModel) tabela.getModel();
            modelo.addRow(linha);

        }
    }//GEN-LAST:event_BtnAdicionarActionPerformed

