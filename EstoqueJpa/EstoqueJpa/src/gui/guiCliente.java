    private void BtnGravarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnGravarActionPerformed
        // TODO add your handling code here:
        Cliente c = new Cliente();
        c.setNome(TxtNome.getText());
        c.setEndereco(TxtEndereco.getText());
        c.setTelefone(TxtTelefone.getText());

        daoCliente.create(c);
        TxtId.setText(c.getId().toString());

        LstCliente.setListData(daoCliente.findClienteEntities().toArray());

        desabilitarCampos();
    }//GEN-LAST:event_BtnGravarActionPerformed

    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        // TODO add your handling code here:
        LstCliente.setListData(daoCliente.findClienteEntities().toArray());
    }//GEN-LAST:event_formWindowOpened

    private void LstClienteValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_LstClienteValueChanged
        c = (Cliente) LstCliente.getSelectedValue(); // TODO add your handling code here:
        TxtId.setText(c.getId().toString());
        TxtNome.setText(c.getNome());
        TxtEndereco.setText(c.getEndereco());
        TxtTelefone.setText(c.getTelefone());
    }//GEN-LAST:event_LstClienteValueChanged

    private void BtnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnExcluirActionPerformed
        try {
            // TODO add your handling code here:
            daoCliente.destroy(c.getId());
            LstCliente.setListData(daoCliente.findClienteEntities().toArray());
            
        } catch (NonexistentEntityException ex) {
            JOptionPane.showMessageDialog(null, "Erro ao tentar excluir o Cliente." + ex);
        }        // TODO add your handling code here:
    }//GEN-LAST:event_BtnExcluirActionPerformed

